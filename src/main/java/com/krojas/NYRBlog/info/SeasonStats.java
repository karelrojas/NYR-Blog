package com.krojas.NYRBlog.info;

import java.util.List;

public class SeasonStats {

	private List<Goalies> goalies;
	private List<Skaters> skaters;

	public List<Goalies> getGoalies() {
		return goalies;
	}

	public void setGoalies(List<Goalies> goalies) {
		this.goalies = goalies;
	}

	public List<Skaters> getSkaters() {
		return skaters;
	}

	public void setSkaters(List<Skaters> skaters) {
		this.skaters = skaters;
	}

	
	
}
